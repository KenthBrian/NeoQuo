// Language Switching
function initializeLanguageSwitcher() {
    const enBtn = document.getElementById('lang-en');
    const tlBtn = document.getElementById('lang-tl');
    
    enBtn.addEventListener('click', () => switchLanguage('en'));
    tlBtn.addEventListener('click', () => switchLanguage('tl'));
}

function switchLanguage(lang) {
    currentLanguage = lang;
    
    // Update button states
    document.getElementById('lang-en').classList.toggle('active', lang === 'en');
    document.getElementById('lang-tl').classList.toggle('active', lang === 'tl');
    
    // Update all text elements
    const elements = document.querySelectorAll('[data-en]');
    elements.forEach(element => {
        const text = element.getAttribute(`data-${lang}`);
        if (text) {
            element.textContent = text;
        }
    });
    
    // Update placeholders
    const nameInput = document.getElementById('child-name');
    if (nameInput) {
        nameInput.placeholder = lang === 'en' 
            ? "Enter child's name" 
            : "Ilagay ang pangalan ng bata";
    }
}

// Facts Slider
function initializeFactsSlider() {
    const factCards = document.querySelectorAll('.fact-card');
    
    setInterval(() => {
        factCards[factsIndex].classList.remove('active');
        factsIndex = (factsIndex + 1) % factCards.length;
        factCards[factsIndex].classList.add('active');
    }, 4000);
}

// Navigation
function initializeNavigation() {
    const ctaButton = document.querySelector('.cta-button');
    const navLinks = document.querySelectorAll('.nav-menu a');
    
    ctaButton.addEventListener('click', () => {
        document.getElementById('clean-water').scrollIntoView({ behavior: 'smooth' });
    });
    
    navLinks.forEach(link => {
        link.addEventListener('click', (e) => {
            e.preventDefault();
            const targetId = link.getAttribute('href');
            const targetElement = document.querySelector(targetId);
            if (targetElement) {
                targetElement.scrollIntoView({ behavior: 'smooth' });
            }
        });
    });
}

// Hamburger Menu
function initializeHamburgerMenu() {
    const hamburger = document.getElementById('hamburger');
    const navRight = document.querySelector('.nav-right');
    const navLinks = document.querySelectorAll('.nav-menu a');
    
    hamburger.addEventListener('click', () => {
        hamburger.classList.toggle('active');
        navRight.classList.toggle('active');
    });
    
    // Close menu when clicking on a link
    navLinks.forEach(link => {
        link.addEventListener('click', () => {
            hamburger.classList.remove('active');
            navRight.classList.remove('active');
        });
    });
    
    // Close menu when clicking outside
    document.addEventListener('click', (e) => {
        if (!hamburger.contains(e.target) && !navRight.contains(e.target)) {
            hamburger.classList.remove('active');
            navRight.classList.remove('active');
        }
    });
}

// Spot the Germs Game
function initializeGermGame() {
    const germs = document.querySelectorAll('.germ');
    const scoreElement = document.getElementById('germs-found');
    
    // Position germs randomly
    germs.forEach(germ => {
        const x = Math.random() * 80 + 10; // 10% to 90%
        const y = Math.random() * 70 + 20; // 20% to 90%
        germ.style.left = x + '%';
        germ.style.top = y + '%';
        
        germ.addEventListener('click', () => {
            if (!germ.classList.contains('found')) {
                germ.classList.add('found');
                germsFound++;
                scoreElement.textContent = germsFound;
                
                if (germsFound === 5) {
                    setTimeout(() => {
                        alert(currentLanguage === 'en' 
                            ? 'Great job! You found all the germs!' 
                            : 'Magaling! Nahanap mo ang lahat ng mikrobyo!');
                        earnBadge('badge-germ-buster');
                    }, 500);
                }
            }
        });
    });
}

// Quiz Game (Drag and Drop)
function initializeQuiz() {
    const scenarios = document.querySelectorAll('.scenario-card');
    const dropZones = document.querySelectorAll('.drop-zone');
    const scoreElement = document.getElementById('quiz-score');
    
    scenarios.forEach(scenario => {
        scenario.addEventListener('dragstart', (e) => {
            e.dataTransfer.setData('text/plain', scenario.dataset.answer);
            scenario.classList.add('dragging');
        });
        
        scenario.addEventListener('dragend', () => {
            scenario.classList.remove('dragging');
        });
    });
    
    dropZones.forEach(zone => {
        zone.addEventListener('dragover', (e) => {
            e.preventDefault();
            zone.classList.add('drag-over');
        });
        
        zone.addEventListener('dragleave', () => {
            zone.classList.remove('drag-over');
        });
        
        zone.addEventListener('drop', (e) => {
            e.preventDefault();
            zone.classList.remove('drag-over');
            
            const draggedAnswer = e.dataTransfer.getData('text/plain');
            const zoneAnswer = zone.dataset.answer;
            
            if (draggedAnswer === zoneAnswer) {
                quizScore++;
                scoreElement.textContent = quizScore;
                
                // Find and hide the dropped scenario
                const draggedElement = document.querySelector(`[data-answer="${draggedAnswer}"].dragging`);
                if (draggedElement) {
                    draggedElement.style.opacity = '0.5';
                    draggedElement.style.pointerEvents = 'none';
                }
                
                if (quizScore === 4) { // Only 4 correct answers expected
                    setTimeout(() => {
                        alert(currentLanguage === 'en' 
                            ? 'Excellent! You know when to wash hands!' 
                            : 'Napakahusay! Alam mo kung kailan hugasan ang kamay!');
                        earnBadge('badge-quiz-master');
                    }, 500);
                }
            }
        });
    });
}

// Puzzle Game
function initializePuzzleGame() {
    const soapItems = document.querySelectorAll('.puzzle-item[data-type="soap"]');
    const handTargets = document.querySelectorAll('.puzzle-target[data-type="hands"]');
    const scoreElement = document.getElementById('puzzle-score');
    
    soapItems.forEach(soap => {
        soap.addEventListener('dragstart', (e) => {
            e.dataTransfer.setData('text/plain', 'soap');
        });
    });
    
    handTargets.forEach(target => {
        target.addEventListener('dragover', (e) => {
            e.preventDefault();
        });
        
        target.addEventListener('drop', (e) => {
            e.preventDefault();
            const draggedType = e.dataTransfer.getData('text/plain');
            
            if (draggedType === 'soap' && !target.classList.contains('correct')) {
                target.classList.add('correct');
                target.innerHTML = 'ü§≤‚ú®'; // Clean hands
                puzzleScore++;
                scoreElement.textContent = puzzleScore;
                
                if (puzzleScore === 3) {
                    setTimeout(() => {
                        alert(currentLanguage === 'en' 
                            ? 'Perfect! Soap makes hands clean!' 
                            : 'Perpekto! Ang sabon ay naglilinis ng kamay!');
                        earnBadge('badge-soap-saver');
                    }, 500);
                }
            }
        });
    });
}

// Memory Game
function initializeMemoryGame() {
    const memoryGameContainer = document.getElementById('memory-game');
    const scoreElement = document.getElementById('memory-score');
    const resetButton = document.getElementById('reset-memory');
    
    const symbols = ['üßº', 'üíß', 'ü¶†', 'ü§≤', 'üöø', 'üßΩ'];
    
    function createMemoryGame() {
        memoryCards = [...symbols, ...symbols].sort(() => Math.random() - 0.5);
        memoryGameContainer.innerHTML = '';
        flippedCards = [];
        canFlip = true;
        memoryScore = 0;
        scoreElement.textContent = memoryScore;
        
        memoryCards.forEach((symbol, index) => {
            const card = document.createElement('div');
            card.className = 'memory-card';
            card.dataset.symbol = symbol;
            card.dataset.index = index;
            card.addEventListener('click', flipCard);
            memoryGameContainer.appendChild(card);
        });
    }
    
    function flipCard(e) {
        const card = e.target;
        
        if (!canFlip || card.classList.contains('flipped') || card.classList.contains('matched')) {
            return;
        }
        
        card.classList.add('flipped');
        card.textContent = card.dataset.symbol;
        flippedCards.push(card);
        
        if (flippedCards.length === 2) {
            canFlip = false;
            
            setTimeout(() => {
                if (flippedCards[0].dataset.symbol === flippedCards[1].dataset.symbol) {
                    flippedCards.forEach(card => {
                        card.classList.add('matched');
                    });
                    memoryScore++;
                    scoreElement.textContent = memoryScore;
                    
                    if (memoryScore === 6) {
                        setTimeout(() => {
                            alert(currentLanguage === 'en' 
                                ? 'Amazing memory! You found all pairs!' 
                                : 'Kamangha-manghang memorya! Nahanap mo ang lahat ng pares!');
                            earnBadge('badge-memory-champ');
                        }, 500);
                    }
                } else {
                    flippedCards.forEach(card => {
                        card.classList.remove('flipped');
                        card.textContent = '';
                    });
                }
                
                flippedCards = [];
                canFlip = true;
            }, 1000);
        }
    }
    
    resetButton.addEventListener('click', createMemoryGame);
    createMemoryGame();
}

