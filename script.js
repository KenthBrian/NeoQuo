// Game Reset Functions
function initializeGameResets() {
    // Reset Germs Game
    document.getElementById('reset-germs').addEventListener('click', resetGermsGame);
    
    // Reset Quiz Game
    document.getElementById('reset-quiz').addEventListener('click', resetQuizGame);
    
    // Reset Puzzle Game
    document.getElementById('reset-puzzle').addEventListener('click', resetPuzzleGame);
}

function resetGermsGame() {
    germsFound = 0;
    document.getElementById('germs-found').textContent = germsFound;
    
    const germs = document.querySelectorAll('.germ');
    germs.forEach(germ => {
        germ.classList.remove('found');
        // Reposition germs randomly
        const x = Math.random() * 80 + 10;
        const y = Math.random() * 70 + 20;
        germ.style.left = x + '%';
        germ.style.top = y + '%';
    });
}

function resetQuizGame() {
    quizScore = 0;
    document.getElementById('quiz-score').textContent = quizScore;
    
    // Reset all scenario cards
    const scenarios = document.querySelectorAll('.scenario-card');
    scenarios.forEach(scenario => {
        scenario.style.opacity = '1';
        scenario.style.pointerEvents = 'auto';
    });
    
    // Clear drop zones
    const dropZones = document.querySelectorAll('.drop-zone');
    dropZones.forEach(zone => {
        const droppedItems = zone.querySelectorAll('.scenario-card');
        droppedItems.forEach(item => item.remove());
    });
}

function resetPuzzleGame() {
    puzzleScore = 0;
    document.getElementById('puzzle-score').textContent = puzzleScore;
    
    // Reset all puzzle targets
    const targets = document.querySelectorAll('.puzzle-target');
    targets.forEach(target => {
        target.classList.remove('correct');
        target.innerHTML = 'ü§≤üí©'; // Reset to dirty hands
    });
    
    // Reset soap items visibility
    const soapItems = document.querySelectorAll('.puzzle-item[data-type="soap"]');
    soapItems.forEach(soap => {
        soap.style.opacity = '1';
        soap.style.pointerEvents = 'auto';
    });
}

